#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			sign;
	long int	num;

	sign = 1;
	num = 0;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		num *= 10;
		num += (*nptr - 48);
		nptr++;
	}
	return (num * sign);
}
